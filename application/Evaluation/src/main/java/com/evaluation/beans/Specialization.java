/*
 * Specialization Beans
 */
package com.evaluation.beans;


public class Specialization {
    private int ids;
    private int idsp;
    private String specializationcol;

    public Specialization() {
    }

    public int getIds() {
        return ids;
    }

    public void setIds(int ids) {
        this.ids = ids;
    }

    public int getIdsp() {
        return idsp;
    }

    public void setIdsp(int idsp) {
        this.idsp = idsp;
    }

    public String getSpecializationcol() {
        return specializationcol;
    }

    public void setSpecializationcol(String specializationcol) {
        this.specializationcol = specializationcol;
    }
    
}
